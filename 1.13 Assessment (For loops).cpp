#include <iostream>
using namespace std;

int main() {
	for (int i = 50; i <= 70; i += 2)
		cout << i << " ";

	cout << endl;
	cout << endl;

	for (int g = 100; g >= 50; g -= 5)
		cout << g << " ";

	cout << endl;
	cout << endl;

	for (int g = 2; g <= 2048; g *= 2)
		cout << g << " ";

	cout << endl;
	cout << endl;
}
