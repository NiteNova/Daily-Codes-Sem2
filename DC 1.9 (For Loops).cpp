#include <iostream>
using namespace std;

int main() {
    for (int i = 70; i <= 80; i += 1)
        cout << i << " ";

    cout << endl;

    for (int g = 100; g <= 300; g += 20)
        cout << g << " ";

    cout << endl;

    for (int t = 25; t >= -25; t -= 5)
        cout << t << " ";

    cout << endl;

    for (int P = 10000; P >= 9; P /= 5)
        cout << P << " ";

}
